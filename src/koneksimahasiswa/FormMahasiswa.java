    private void setupTable() {
        // Setup model tabel dengan kolom yang sesuai
        DefaultTableModel model = new DefaultTableModel(
            new Object[][]{},
            new String[]{"NIM", "Nama", "Jurusan", "Alamat"}
        ) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false; // Membuat tabel tidak bisa diedit langsung
            }
        };
        tblMahasiswa.setModel(model);
        
        // Mouse listener untuk tabel - YANG BENAR
        tblMahasiswa.addMouseListener(new java.awt.event.MouseAdapter() {
            public void mouseClicked(java.awt.event.MouseEvent evt) {
                pilihDataDariTabel();
            }
        });
    }

    private void loadDataToTable() {
        DefaultTableModel model = (DefaultTableModel) tblMahasiswa.getModel();
        model.setRowCount(0); // Clear existing data

        List<Mahasiswa> listMhs = mhsDAO.getAllMahasiswa();
        for (Mahasiswa mhs : listMhs) {
            Object[] rowData = {
                mhs.getNim(),
                mhs.getNama(),
                mhs.getJurusan(),
                mhs.getAlamat()
            };
            model.addRow(rowData);
        }
    }

    private boolean validasiInput() {
        if (txtNIM.getText().trim().isEmpty() ||
            txtNama.getText().trim().isEmpty() ||
            cmbJurusan.getSelectedItem() == null ||
            txtAlamat.getText().trim().isEmpty()) {
            
            JOptionPane.showMessageDialog(this, 
                "Field NIM, Nama, Jurusan, dan Alamat harus diisi!\nField Phone bersifat opsional.", 
                "Validasi Error", 
                JOptionPane.WARNING_MESSAGE);
            return false;
        }
        return true;
    }

    private void clearForm() {
        txtNIM.setText("");
        txtNama.setText("");
        cmbJurusan.setSelectedIndex(0);
        txtAlamat.setText("");
        txtPhone.setText("");
        tblMahasiswa.clearSelection();
    }

    private void pilihDataDariTabel() {
        int selectedRow = tblMahasiswa.getSelectedRow();
        if (selectedRow != -1) {
            DefaultTableModel model = (DefaultTableModel) tblMahasiswa.getModel();
            txtNIM.setText(model.getValueAt(selectedRow, 0).toString());
            txtNama.setText(model.getValueAt(selectedRow, 1).toString());
            cmbJurusan.setSelectedItem(model.getValueAt(selectedRow, 2).toString());
            txtAlamat.setText(model.getValueAt(selectedRow, 3).toString());
        }
    }

