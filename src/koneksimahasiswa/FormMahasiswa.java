    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
        // TODO add your handling code here:
        if (validasiInput()) {
            String nim = txtNIM.getText().trim();
            String nama = txtNama.getText().trim();
            String jurusan = cmbJurusan.getSelectedItem().toString();
            String alamat = txtAlamat.getText().trim();
            
            Mahasiswa mhs = new Mahasiswa(nim, nama, jurusan, alamat);
            mhsDAO.tambahMahasiswa(mhs);
            clearForm();
            loadDataToTable();
            JOptionPane.showMessageDialog(this, "Data berhasil ditambahkan!");
        }
    }//GEN-LAST:event_btnTambahActionPerformed

    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblMahasiswa.getSelectedRow();
    if (selectedRow == -1) {
        JOptionPane.showMessageDialog(this, 
            "Pilih data yang akan diedit dari tabel!", 
            "Peringatan", 
            JOptionPane.WARNING_MESSAGE);
        return;
    }
    
    // Validasi hanya field yang diperlukan (tanpa Phone)
    if (txtNIM.getText().trim().isEmpty() ||
        txtNama.getText().trim().isEmpty() ||
        cmbJurusan.getSelectedItem() == null ||
        txtAlamat.getText().trim().isEmpty()) {
        
        JOptionPane.showMessageDialog(this, 
            "Field NIM, Nama, Jurusan, dan Alamat harus diisi!\nField Phone bersifat opsional.", 
            "Validasi Error", 
            JOptionPane.WARNING_MESSAGE);
        return;
    }
    
    String nimLama = tblMahasiswa.getValueAt(selectedRow, 0).toString();
    String nimBaru = txtNIM.getText().trim();
    
    Mahasiswa mhs = new Mahasiswa(
        nimBaru,
        txtNama.getText().trim(),
        cmbJurusan.getSelectedItem().toString(),
        txtAlamat.getText().trim()
    );
    
    mhsDAO.updateMahasiswa(mhs, nimLama);
    clearForm();
    loadDataToTable();
    JOptionPane.showMessageDialog(this, "Data berhasil diedit!");
    }//GEN-LAST:event_btnEditActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblMahasiswa.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, 
                "Pilih data yang akan dihapus dari tabel!", 
                "Peringatan", 
                JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        String nim = tblMahasiswa.getValueAt(selectedRow, 0).toString();
        String nama = tblMahasiswa.getValueAt(selectedRow, 1).toString();
        
        int confirm = JOptionPane.showConfirmDialog(this, 
            "Yakin ingin menghapus data:\nNIM: " + nim + "\nNama: " + nama + "?", 
            "Konfirmasi Hapus", 
            JOptionPane.YES_NO_OPTION);
        
        if (confirm == JOptionPane.YES_OPTION) {
            mhsDAO.hapusMahasiswa(nim);
            clearForm();
            loadDataToTable();
            JOptionPane.showMessageDialog(this, "Data berhasil dihapus!");
        }
    }//GEN-LAST:event_btnHapusActionPerformed

